namespace ForumApp.Models;

/// <summary>
/// Изброен тип, дефиниращ възможните статуси на коментар в системата за модериране.
/// Статусът се определя първоначално от ML модела за класификация на токсичност
/// и може да бъде променен впоследствие от модератор.
/// </summary>
public enum CommentStatus
{
    /// <summary>
    /// Коментарът е одобрен и е видим за всички потребители на форума.
    /// Този статус се присвоява, когато ML моделът класифицира съдържанието като безопасно.
    /// </summary>
    Approved,

    /// <summary>
    /// Коментарът е в изчакване за преглед от модератор.
    /// Този статус се присвоява, когато ML моделът открие потенциално токсично съдържание,
    /// но крайното решение се оставя на човешка преценка.
    /// </summary>
    Pending,

    /// <summary>
    /// Коментарът е отхвърлен и не е видим за обикновените потребители.
    /// Този статус се присвоява при категорично токсично съдържание или
    /// след решение на модератор за неприемливост на коментара.
    /// </summary>
    Rejected
}
